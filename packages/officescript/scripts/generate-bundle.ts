/**
 * generate-bundle.ts — checked-in generator for the Office Script port.
 *
 * Office Script *is* TypeScript, so we do NOT re-port the SheetCompressor core.
 * Instead we bundle the verified TS reference modules (the ones covered by the
 * 288-test suite) into a single `.osts` `namespace SheetCompressorInternal`,
 * byte-for-byte derived from the source. Only `main()` host glue and chart
 * extraction are hand-written.
 *
 * It emits three artifacts under `packages/officescript/src/`:
 *
 *   1. SheetCompressorInternal.generated.osts — the core namespace ONLY. This
 *      is the drift-guarded artifact (mirror of `prompts.test.ts`): the
 *      driftguard test re-runs this generator in memory and asserts the
 *      committed file matches byte-for-byte.
 *
 *   2. SheetCompressor.osts — the shippable script: a minimal
 *      `declare namespace ExcelScript` host-types block + the generated core
 *      namespace + a hand-written `main(workbook, sheetName?)` host glue.
 *
 *   3. conformance.osts — embeds every `fixtures/corpus/*` input + goldens as
 *      inlined literals, runs `SheetCompressorInternal.compress` over each, and
 *      prints a PASS/FAIL summary. A human pastes this into Excel Online to
 *      sign off (gate-style).
 *
 * The bundle must be ExcelScript-pure: no `node:*` imports, no `require`, no
 * `import.meta`. The gpt-tokenizer parts of `tokens.ts` are therefore excluded
 * (only `estimateTokens` + the `TokenCounter` type are inlined); `compress()`
 * defaults to `estimateTokens`, which is exactly what conformance uses.
 */

